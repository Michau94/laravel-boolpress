    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {

        $request->validate(['roles' => 'nullable| exists:roles,id']);

        if (!$request->roles)
            $user->roles()->detach();
        else
            $user->roles()->sync($request->roles);

        // non c'Ã¨ bisogno di update in quanto non si agisce direttamente sulla tabella users o roles

        return redirect()->route('admin.users.index')->with('alert-type', 'success')->with('alert-message', 'Role modified with success!');
    }

